package com.tasks.homework5;

public class Adress {
	
	String country;
	String region;
	String city;
	String street;
	int house;
	
	/**
	 * @param country
	 * @param region
	 * @param city
	 * @param street
	 * @param house
	 */
	public Adress(String country, String region, String city, String street, int house) {
		super();
		this.country = country;
		this.region = region;
		this.city = city;
		this.street = street;
		this.house = house;
	}
	


}
